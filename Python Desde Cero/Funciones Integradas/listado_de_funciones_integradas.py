# Funciones Numéricas y Matemáticas

#abs(x) # Devuelve el valor absoluto de x.

#divmod(a, b) # Devuelve el cociente y el residuo de la división a ÷ b.

#pow(x, y[, z]) # Devuelve x elevado a la potencia y, opcional módulo z.

#round(number[, ndigits]) # Redondea un número a ndigits decimales.

#sum(iterable, start=0) # Suma todos los elementos del iterable, empezando en start.

#max(iterable, *args) # Devuelve el valor más grande.

#min(iterable, *args) # Devuelve el valor más pequeño.




# Funciones de Cadenas y Representación

#ascii(obj) – Devuelve una representación en ASCII de un objeto.

#bin(x) – Convierte un número a binario (0b).

#chr(i) – Convierte un número Unicode en un carácter.

#hex(x) – Convierte un número a hexadecimal (0x).

#oct(x) – Convierte un número a octal (0o).

#ord(c) – Devuelve el código Unicode de un carácter.

#repr(obj) – Devuelve una representación legible del objeto.

#str(obj) – Convierte un objeto a string.

#format(value, format_spec) – Da formato a cadenas de texto o números.




#Funciones de Colecciones
#all(iterable) – Devuelve True si todos los elementos son verdaderos.

#any(iterable) – Devuelve True si al menos uno es verdadero.

#enumerate(iterable, start=0) – Devuelve índice y valor al recorrer un iterable.

#filter(func, iterable) – Filtra elementos según una condición.

#map(func, iterable) – Aplica una función a cada elemento.

#zip(*iterables) – Combina varios iterables en tuplas paralelas.

#sorted(iterable, ...) – Devuelve una lista ordenada.

#reversed(seq) – Devuelve un iterador con los elementos al revés.




#Funciones de Tipo y Conversión

#bool(x) – Convierte a valor booleano (True o False).

#int(x[, base]) – Convierte a entero.

#float(x) – Convierte a número decimal.

#complex([real[, imag]]) – Crea un número complejo.

#list(iterable) – Convierte a lista.

#tuple(iterable) – Convierte a tupla.

#set(iterable) – Crea un conjunto (sin duplicados).

#frozenset(iterable) – Conjunto inmutable.

#dict(**kwargs) – Crea un diccionario.

#bytes() – Crea un arreglo de bytes inmutable.

#bytearray() – Crea un arreglo de bytes mutable.

#memoryview(obj) – Crea una vista de memoria de un objeto de bytes.




#Inspección de Objetos y Atributos

#callable(obj) – Verifica si un objeto se puede llamar (como una función).

#getattr(obj, name[, default]) – Obtiene el atributo de un objeto.

#hasattr(obj, name) – Verifica si el objeto tiene ese atributo.

#setattr(obj, name, value) – Establece un atributo a un objeto.

#delattr(obj, name) – Elimina un atributo de un objeto.

#isinstance(obj, class) – Verifica si un objeto es instancia de una clase.

#issubclass(class, classinfo) – Verifica si una clase es subclase de otra.




#Iteradores

#iter(obj[, sentinel]) – Devuelve un iterador del objeto.

#next(iterator[, default]) – Devuelve el siguiente elemento de un iterador.

#slice(start, stop[, step]) – Crea un objeto de rebanado (slicing).




#Funciones de Información / Sistema

#dir([obj]) – Lista los atributos y métodos de un objeto.

#globals() – Devuelve un diccionario con todas las variables globales.

#locals() – Devuelve un diccionario con todas las variables locales.

#vars([obj]) – Devuelve el __dict__ del objeto.

#help([obj]) – Muestra la documentación de Python o de un objeto.

#type(obj) – Devuelve el tipo del objeto.

#id(obj) – Devuelve la dirección de memoria del objeto.#




#Funciones Avanzadas / Meta-programación

#eval(expr) – Evalúa una expresión de Python desde un string.

#exec(obj) – Ejecuta código Python dinámicamente.

#compile(source, filename, mode) – Compila código en objeto ejecutable.

#__import__(name) – Importa un módulo dinámicamente.




